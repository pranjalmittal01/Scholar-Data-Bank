function fileValidation1() {
    var fileInput = document.getElementById('file1');
     
    var filePath = fileInput.value;
 
    // Allowing file type
    var allowedExtensions = /(\.pdf)$/i;
     
    if (!allowedExtensions.exec(filePath)) {
        alert('Invalid file type');
        fileInput.value = '';
        return false;
    }
}

